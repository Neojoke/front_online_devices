export let initMap=function(container) {
    //初始化地图
    var map = new TMap.Map(container, {
        zoom: 11.5, //设置地图缩放级别
        center: new TMap.LatLng(39.914961477236766, 116.3898104098216), //设置地图中心点坐标
        mapStyleId: 'style7', //个性化样式2
        renderOptions: {
            enableBloom: true, //泛光
        },
        baseMap: {
            type: "vector",
            features: ["base", "building3d", "label", "point"],
          },
    });
    //初始化辐射圈
    var radiationCircle = new TMap.visualization.Radiation({
        styles: {
            style2: {
                fillColor: 'rgba(29,250,242,0.9)', //辐射圈填充颜色
            },
        },
        processAnimation: {
            duration: 3000, //辐射圈半径从0到最大半径的时长，单位为ms
        },
    }).addTo(map);
    // radiationCircle.setData(radiationData);

    //初始化散点图并添加至map图层
    var dot = new TMap.visualization.Dot({
        styles: {
            style1: {
                radius: 2, //圆形半径
            },
            style2: {
                fillColor: 'rgba(29,250,242,1)', //圆形填充颜色
                radius: 3, //圆形半径
            },
        },
        enableBloom: true, // 泛光
    }).addTo(map);
    // dot.setData(dotData); //设置数据
    return {radiationCircle,dot}
}